void MyPolygon::Line (CDC *pDC, const CPoint &a, const CPoint &b)
{
	int x1 = a.x;
	int y1 = a.y;

	int x2 = b.x;
	int y2 = b.y;

	int FlipY = 0;
	int Flip45 = 0;

	if(x1 > x2)
	{
		int temp = x1;
		x1 = x2;
		x2 = temp;

		temp = y2;
		y2 = y1;
		y1 = temp;
	}

	int dx = x2 - x1;
	int dy = y2 - y1;

	if(dy < 0)
	{
		FlipY = 1;
		dy = -dy;
		y1 = -y1;
		y2 = -y2;
	}

	if(dy > dx)
	{
		int temp = dx;
		dx = dy;
		dy = temp;
		temp = x1;
		x1 = y1;
		y1 = temp;
		temp = y2;
		y2 = x2;
		x2 = temp;	
		Flip45 = 1;
	}

	int x = x1;
	int y = y1;

	int d = dy + dy - dx;

	int deltaPostive = d - dx;
	int delteNegative = dy + dy; 

	CPoint point;

	while(x <= x2)
	{
		point.x = x;
		point.y = y;
		if(Flip45)
		{
			int temp = point.x;
			point.x = point.y;
			point.y = temp;
		}
		if(FlipY)
		{
			point.y = -point.y;
		}

		pDC->SetPixel(point, 0x0);

		if(d >= 0)
		{
			d += deltaPostive;
			++ x;
			++ y;
		}
		else
		{
			d += delteNegative;
			++ x;
		}
	}
}

